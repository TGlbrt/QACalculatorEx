package com.qa.calculator;

public class Calculator {

	public int add(int num1, int num2) {
		return num1 + num2;
	}
	
	public int sub(int numberOne, int numberTwo) {
		return numberOne - numberTwo;
	}
	
	public double sub(double numberOne, double numberTwo) {
		return numberOne - numberTwo;
	}
	
	

}
